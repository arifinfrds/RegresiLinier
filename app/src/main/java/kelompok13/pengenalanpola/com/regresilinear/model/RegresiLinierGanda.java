public class RegresiLinierGanda {

    private double x1;
    private double x2;
    private double y;

    public RegresiLinierGanda(double x1, double x2, double y) {
        this.x1 = x1;
        this.x2 = x2;
        this.y = y;
    }

    public double getX1() {
        return x1;
    }

    public double getX2() {
        return x2;
    }

    public double getY() {
        return y;
    }
}
